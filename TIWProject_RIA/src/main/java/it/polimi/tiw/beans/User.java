package it.polimi.tiw.beans;

public class User {
	private String username;
	private String email;
	
	public String getUsername() {
		return username;
	}
	
	public String getEmail(){
		return email;
	}
	
	public void setUsername(String usr){
		username = usr;
	}
	
	public void setEmail(String em) {
		email = em;
	}
}
